package hsw.gradle.template;

public class Multiplikation {
    public int multiply(int a, int b) {
        return a * b;
    }
    public double multiply(int a, double b) {
        return a * b;
    }
    public double multiply(double a, int b) {
        return a * b;
    }
    public double multiply(double a, double b) {
        return a * b;
    }
}
