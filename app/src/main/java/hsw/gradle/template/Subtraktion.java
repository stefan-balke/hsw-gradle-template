package hsw.gradle.template;

public class Subtraktion {
    public int subtract(int a, int b) {
        return a - b;
    }
    public double subtract(int a, double b) {
        return a - b;
    }
    public double subtract(double a, int b) {
        return a - b;
    }
    public double subtract(double a, double b) {
        return a - b;
    }
}
